	public static void main(String[] args) {
		//initalizing for loops
		for (int i = 1;i <= 5 ;i++ ) {
			System.out.println();
			for (int j = 1;j <=i ;j++ ) {
				System.out.print(" * ");
				
			}
			
		}
		for (int i = 4;i >= 0 ;i-- ) {
			System.out.println();
			for (int j = 1;j <=i ;j++ ) {
				System.out.print(" * ");
	}
}
}
